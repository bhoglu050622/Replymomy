// Runs after `next build` on Hostinger's server.
// 1. Patches .next/standalone/server.js with a port pre-check so only ONE
//    worker binds port 3000 — extras exit cleanly (code 0, no Passenger respawn).
// 2. Patches .htaccess with PassengerMaxPoolSize 1.
// 3. Removes restart.txt (Hostinger creates it on deploy; deleting it prevents
//    the initial multi-spawn storm before the first request is served).
import { existsSync, readFileSync, writeFileSync, unlinkSync } from "fs";
import { resolve } from "path";

const HOSTINGER_MARKER = "/home/u228387150/domains/replymommy.com/public_html/.htaccess";

if (!existsSync(HOSTINGER_MARKER)) {
  // Running locally — nothing to patch
  process.exit(0);
}

// ── 1. Patch standalone server.js ────────────────────────────────────────────
// .next/standalone/server.js is generated by `next build` and copied by
// Hostinger to nodejs/server.js. Patch it here so every deploy gets the fix.
const STANDALONE = resolve(".next/standalone/server.js");

if (existsSync(STANDALONE)) {
  let src = readFileSync(STANDALONE, "utf8");
  const PATCH_MARKER = "// rm-patch: single-worker port guard";

  if (!src.includes(PATCH_MARKER)) {
    // Inject a TCP pre-check: if the port is already bound, exit cleanly so
    // Passenger routes traffic to the existing worker instead of respawning.
    const GUARD = `
${PATCH_MARKER}
;(function guardSingleWorker() {
  const net = require("net");
  const port = parseInt(process.env.PORT, 10) || 3000;
  const host = process.env.HOSTNAME || "0.0.0.0";
  const tester = net.createServer();
  tester.once("error", function(err) {
    if (err.code === "EADDRINUSE") {
      // Another worker already owns this port — exit cleanly.
      process.exit(0);
    }
    // Any other error: let the app handle it.
  });
  tester.once("listening", function() {
    tester.close(); // Free the port for startServer()
  });
  tester.listen(port, host);
})();
`;

    // Insert before the startServer call
    src = src.replace(/startServer\(\{/, `${GUARD}\nstartServer({`);
    writeFileSync(STANDALONE, src, "utf8");
    console.log("Patched .next/standalone/server.js with single-worker guard");
  } else {
    console.log(".next/standalone/server.js already patched");
  }
} else {
  console.warn("Warning: .next/standalone/server.js not found — skipping patch");
}

// ── 2. Patch .htaccess ───────────────────────────────────────────────────────
const HTACCESS = HOSTINGER_MARKER;
let htContent = readFileSync(HTACCESS, "utf8");

if (!htContent.includes("PassengerMaxPoolSize")) {
  htContent = htContent.replace(
    "RewriteRule",
    "PassengerMaxPoolSize 1\nPassengerMinInstances 1\nRewriteRule"
  );
  writeFileSync(HTACCESS, htContent);
  console.log("Patched .htaccess with PassengerMaxPoolSize 1");
} else {
  console.log(".htaccess already has PassengerMaxPoolSize");
}

// ── 3. Remove restart.txt ────────────────────────────────────────────────────
// Hostinger creates this during deploy. Deleting it here means Passenger
// won't trigger an extra restart storm after the build completes.
const RESTART_TXT = "/home/u228387150/domains/replymommy.com/nodejs/tmp/restart.txt";
if (existsSync(RESTART_TXT)) {
  unlinkSync(RESTART_TXT);
  console.log("Removed restart.txt");
}
